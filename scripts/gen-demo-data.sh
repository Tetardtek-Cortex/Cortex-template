// Run: bash scripts/gen-demo-data.sh

export const DEMO_DATA = {
  agents: {
    total: $TOTAL,
    free: $FREE,
    pro: $PRO,
    owner: $OWNER,
  },
  sessions: $SESSIONS,
  scripts: $SCRIPTS,
  docs: $DOCS,
  projects: [
    {
      name: 'my-saas-app',
      description: 'SaaS multi-tenant — exemple a remplacer',
      status: 'scaffold',
    },
    {
      name: 'my-game',
      description: 'Jeu indie — exemple a remplacer',
      status: 'scaffold',
    },
    {
      name: 'my-website',
      description: 'Portfolio / vitrine — exemple a remplacer',
      status: 'scaffold',
    },
  ],
  activity: [
    { action: 'Brain installe — template fork', time: 'Maintenant' },
    { action: 'PATHS.md configure', time: 'Au setup' },
    { action: 'Premier boot — session explore', time: 'A venir' },
    { action: 'Premier sprint — brain boot mode work/...', time: 'A venir' },
  ],
  cosmosNodes: [
    { id: 'kernel', label: 'KERNEL', x: 0, y: 0, z: 0, size: 2.5, color: '#f25c7a' },
    { id: 'agents', label: 'Agents', x: 3, y: 1, z: -1, size: 1.8, color: '#c9a0ff' },
    { id: 'sessions', label: 'Sessions', x: -2, y: 2, z: 1, size: 1.5, color: '#a4b4ff' },
    { id: 'projects', label: 'Projets', x: 1, y: -2, z: 2, size: 1.5, color: '#89dceb' },
    { id: 'scripts', label: 'Scripts', x: -3, y: -1, z: -2, size: 1.3, color: '#f25c7a' },
    { id: 'wiki', label: 'Wiki', x: 2, y: 3, z: 0, size: 1.2, color: '#e8c87a' },
    { id: 'docs', label: 'Docs', x: -1, y: -3, z: -1, size: 1.2, color: '#fb923c' },
    { id: 'brain-engine', label: 'Engine', x: 4, y: 0, z: 1, size: 1.4, color: '#9adba8' },
    { id: 'bsi', label: 'BSI', x: -2, y: 1, z: -3, size: 1.3, color: '#a4b4ff' },
    { id: 'tier', label: 'Tier Gate', x: 0, y: 3, z: 2, size: 1.1, color: '#ff4da6' },
  ],
  cosmosEdges: [
    { from: 'kernel', to: 'agents' },
    { from: 'kernel', to: 'sessions' },
    { from: 'kernel', to: 'scripts' },
    { from: 'kernel', to: 'tier' },
    { from: 'agents', to: 'sessions' },
    { from: 'agents', to: 'projects' },
    { from: 'sessions', to: 'bsi' },
    { from: 'projects', to: 'docs' },
    { from: 'scripts', to: 'brain-engine' },
    { from: 'brain-engine', to: 'bsi' },
    { from: 'wiki', to: 'docs' },
    { from: 'tier', to: 'agents' },
  ],
  docsList: [
${DOCS_LIST}  ],
}
TSEOF

echo "✅ demo.ts généré — agents: $TOTAL ($FREE free / $PRO pro / $OWNER owner) | sessions: $SESSIONS | scripts: $SCRIPTS | docs: $DOCS"
