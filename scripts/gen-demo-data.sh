#!/bin/bash
# gen-demo-data.sh — Génère brain-ui/src/data/demo.ts depuis les sources réelles
# Exécuter après chaque ajout/retrait d'agent, script ou doc
#
# Usage : bash scripts/gen-demo-data.sh

set -euo pipefail

BRAIN_ROOT="$(git -C "$(dirname "$0")" rev-parse --show-toplevel)"
DEMO_FILE="$BRAIN_ROOT/brain-ui/src/data/demo.ts"
CATALOG="$BRAIN_ROOT/agents/CATALOG.yml"

# --- Comptages dynamiques ---

if command -v python3 &>/dev/null && python3 -c "import yaml" &>/dev/null 2>&1; then
  read -r TOTAL FREE PRO OWNER <<< "$(python3 - "$CATALOG" <<'PYEOF'
import yaml, sys
with open(sys.argv[1]) as f:
    agents = yaml.safe_load(f).get('agents', [])
tiers = {}
for a in agents:
    t = a.get('tier', 'free')
    tiers[t] = tiers.get(t, 0) + 1
free = tiers.get('free', 0) + tiers.get('featured', 0)
pro = tiers.get('pro', 0)
owner = tiers.get('full', 0) + tiers.get('owner', 0)
total = free + pro + owner
print(f"{total} {free} {pro} {owner}")
PYEOF
)"
else
  echo "⚠️  python3 + pyyaml requis pour lire CATALOG.yml" >&2
  exit 1
fi

SESSIONS=$(ls "$BRAIN_ROOT"/contexts/session-*.yml 2>/dev/null | wc -l)
SCRIPTS=$(ls "$BRAIN_ROOT"/scripts/*.sh "$BRAIN_ROOT"/scripts/*.py 2>/dev/null | wc -l)
DOCS=$(ls "$BRAIN_ROOT"/docs/*.md 2>/dev/null | wc -l)

# --- Générer docsList depuis les fichiers docs/ ---
DOCS_LIST=""
for f in "$BRAIN_ROOT"/docs/*.md; do
  [ -f "$f" ] || continue
  filename="$(basename "$f")"
  [ "$filename" = "README.md" ] && continue
  id="${filename%.md}"
  # Extraire le titre depuis le premier # du fichier
  title="$(head -5 "$f" | grep -m1 '^#' | sed 's/^#\+ //' | sed "s/'/\\\\'/g")"
  [ -z "$title" ] && title="$id"
  DOCS_LIST="${DOCS_LIST}    { id: '$id', title: '$title', file: '$filename' },
"
done

# --- Écrire demo.ts ---
cat > "$DEMO_FILE" <<TSEOF
